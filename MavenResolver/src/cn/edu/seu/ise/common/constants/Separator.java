package cn.edu.seu.ise.common.constants;

/**
 * Common separators. 
 * 
 * @author Dong Qiu
 *
 */
public class Separator {
	
	/** Double underline */
	public final static String D_UL = "__";
	
	/** Underline */
	public final static String UL = "_";
	
	/** Blank */
	public final static String BLANK = " ";
	
	/** Dot */
	public final static String DOT = ".";
	
	/** Slash */
	public final static String SLASH = "/";
	
	/** Colon */
	public final static String COLON = ":";
	
	/** Left parentheses */
	public final static String L_PAR = "(";
	
	/** Right parentheses */
	public final static String R_PAR = ")";
	
	/** Comma */
	public final static String COMMA = ",";
	
	/** Method invocation array */
	public final static String CALL_ARRAY = "-->";
	
	/** New line */
	public final static String NEWLINE = "\n";
	
	/** Tab */
	public final static String TAB = "\t";
}
